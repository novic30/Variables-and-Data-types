public class circle {
    public static void main(String[] args) {
        int radius = 10;
        double area = 22.0 / 7 * radius * radius; // Changed to double for more accurate division
        double circumference = 22.0 / 7 * radius * 2; // Changed to double for more accurate division

        System.out.println("Area = " + area);
        System.out.println("Circumference = " + circumference);

    }
}
